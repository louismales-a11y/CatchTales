  String tr(String key) {

    return _allTranslations[_lang]?[key] ?? _allTranslations['en']?[key] ?? key;
  }

  /// Translate with parameter substitution. Replace {param} placeholders with values.
  String trp(String key, Map<String, String> params) {
    String text = tr(key);
    for (final entry in params.entries) {
      text = text.replaceAll('{${entry.key}}', entry.value);
    }
    return text;
  }

  Future<void> setLanguage(String lang) async {
    _lang = lang;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('app_language', lang);
    notifyListeners();
  }

  Future<void> loadLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    _lang = prefs.getString('app_language') ?? 'en';
    notifyListeners();
  }

  static Future<bool> isFirstLaunch() async {
    final prefs = await SharedPreferences.getInstance();
    return !prefs.containsKey('app_language');
  }
